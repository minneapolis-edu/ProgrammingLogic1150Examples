"""A lot of the techniques for working with strings work with lists too """

# Example operations with a list

streets = ['11th', 'Lake', 'Grand']

first_item = streets[0]
third_item = streets[2]

streets_times_three = streets * 3

print(streets)
print(first_item)
print(third_item)
print(streets_times_three)

print(len(streets))


# Example operations with a string

street = 'Lake'

first_letter = street[0]
third_letter = street[2]

street_times_three = street * 3

print(street)
print(first_letter)
print(third_letter)
print(street_times_three)

print(len(street))




