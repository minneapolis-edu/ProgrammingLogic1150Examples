""" Adding items to a list with append()  """

villains = ['Catwoman', 'Darth Vader', 'Sauron']
print(villains)

villains.append('Lex Luthor')
print(villains)  # ['Catwoman', 'Darth Vader', 'Sauron', 'Lex Luthor']

villains.append('Magneto')
print(villains)  # ['Catwoman', 'Darth Vader', 'Sauron', 'Lex Luthor', 'Magneto']

